function changeLanguage(lang){
	CURRENT_LANGUAGE = lang;
	document.querySelector("#language_label>span").innerHTML = lang;
	document.querySelector("#language_label>img").src = "img/languages/"+lang+".png";
	updateData();
}

function updateData(){
	document.querySelector("#header>h2").innerHTML = GENERAL[CURRENT_LANGUAGE].head;
	document.querySelector("#portfolio>h2").innerHTML = GENERAL[CURRENT_LANGUAGE].portfolio.toUpperCase();
	document.querySelector("a[href='#portfolio']").innerHTML = GENERAL[CURRENT_LANGUAGE].portfolio;
	document.querySelector("#skillset>h2").innerHTML = GENERAL[CURRENT_LANGUAGE].skillset.toUpperCase();
	document.querySelector("a[href='#skillset']").innerHTML = GENERAL[CURRENT_LANGUAGE].skillset;
	document.querySelector("#experience>h2").innerHTML = GENERAL[CURRENT_LANGUAGE].experience.toUpperCase();
	document.querySelector("a[href='#experience']").innerHTML = GENERAL[CURRENT_LANGUAGE].experience;
	document.querySelector("#contact>h2").innerHTML = GENERAL[CURRENT_LANGUAGE].contact.toUpperCase();
	document.querySelector("a[href='#contact']").innerHTML = GENERAL[CURRENT_LANGUAGE].contact;
	document.querySelector("#footer>small").innerHTML = GENERAL[CURRENT_LANGUAGE].footer;

	document.querySelector("#at").innerHTML = GENERAL[CURRENT_LANGUAGE].at;
	document.querySelector("#role").innerHTML = GENERAL[CURRENT_LANGUAGE].role;
	document.querySelector("#about").innerHTML = GENERAL[CURRENT_LANGUAGE].about;
}

function changePorfolio(param){
	let currentPortfolio = document.querySelector("#currentPortfolio").value;
	let index = 0;
	if(param == "minus"){
		index = (currentPortfolio != 0) ? --currentPortfolio : (porfolio[CURRENT_LANGUAGE].length - 1);
	}else{
		index = (currentPortfolio == (porfolio[CURRENT_LANGUAGE].length - 1)) ? 0 : ++currentPortfolio;
	}
	showPorfolio(index);
}

function changeExperience(param){
	let currentExperience = document.querySelector("#currentExperience").value;
	let index = 0;
	if(param == "minus"){
		index = (currentExperience != 0) ? --currentExperience : (experience[CURRENT_LANGUAGE].length - 1);
	}else{
		index = (currentExperience == (experience[CURRENT_LANGUAGE].length - 1)) ? 0 : ++currentExperience;
	}
	showResume(index);
}

function showPorfolio(index){
	document.querySelector("#currentPortfolio").value=index;
	document.querySelector("#portfolioModalLabel").innerHTML=porfolio[CURRENT_LANGUAGE][index].title;
	document.querySelector("#portfolioModalImage").src=porfolio[CURRENT_LANGUAGE][index].img;
	document.querySelector("#portfolioModalDescription").innerHTML=porfolio[CURRENT_LANGUAGE][index].description;
	document.querySelector("#portfolioModalLink").href=porfolio[CURRENT_LANGUAGE][index].link;
	document.querySelector("#portfolioModalCompany").innerHTML=porfolio[CURRENT_LANGUAGE][index].at;
	document.querySelector("#portfolioModalRole").innerHTML=porfolio[CURRENT_LANGUAGE][index].role;
}

function showResume(index){
	document.querySelector("#currentExperience").value=index;
	document.querySelector("#resumeModalLabel").innerHTML=experience[CURRENT_LANGUAGE][index].title;
	document.querySelector("#resumeModalImage").src=experience[CURRENT_LANGUAGE][index].img;
	document.querySelector("#resumeModalDescription").innerHTML=experience[CURRENT_LANGUAGE][index].description;
	document.querySelector("#resumeModalLink").href=experience[CURRENT_LANGUAGE][index].link;

	let html = "";
	for(let i = 0; i<experience[CURRENT_LANGUAGE][index].tags.length; i++){
		html += "<span>"+experience[CURRENT_LANGUAGE][index].tags[i]+"</span>";
	}
	document.querySelector("#resumeModalTags").innerHTML=html;

}